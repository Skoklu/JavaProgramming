package day05_Concatenation;

public class StudentInfo {

    public static void main(String[] args) {

        String studentName = "Jane";
        byte age = 19;
        char gender = 'F';
        String schoolName = "Cydeo";
        String phoneNumber = "(123) 546-4576";
        boolean fullTime = false;


        /*you don't start with a DIGIT (1fullName) to variable names,
         but you can add DIGIT end of the variable names (fullName1)
         */
        System.out.println("studentName = " + studentName);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("schoolName = " + schoolName);
        System.out.println("phoneNumber = " + phoneNumber);
        System.out.println("fullTime = " + fullTime);




    }
}

