package day04_Variables;

public class Currencies {

    public static void main(String[] args) {
        
        int dollar = 1000;
        
        double lira = dollar * 13.45;
        double euro = dollar * 0.88;
        double ruble = dollar * 76.09;
        double pound = dollar * 0.73;
        double peso = dollar * 51.24;
        double cad = dollar * 1.25;
        double jpy = dollar * 114.59;
        double riyal = dollar * 0.39;
        //jpy, pound, cad, riyal...

        System.out.println("dollar = " + dollar);
        System.out.println("lira = " + lira);
        System.out.println("euro = " + euro);
        System.out.println("ruble = " + ruble);
        System.out.println("pound = " + pound);
        System.out.println("peso = " + peso);
        System.out.println("cad = " + cad);
        System.out.println("jpy = " + jpy);
        System.out.println("riyal = " + riyal);
        
        
        
        
        
        
        
        
    }
}
